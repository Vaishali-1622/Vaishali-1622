package AssignmentPrograms;

public class Assignmentwhileloop1 {

	public static void main(String[] args) {
		System.out.println("............Count Number Of Digits........\n");
		int i=0,counter=0;
		while(i<5)
		{
			System.out.println(i+" ");
			i++;
			counter=counter+1;

		}
		System.out.println("Count of Digits: "+counter);


	}

}

package AssignmentPrograms;

public class Assignmentforloop3 {

	public static void main(String[] args) {
		System.out.println("............Sum of n Natural numbers........\n");
		int n=20, sum=0;
		
		for(int i=0;i<=n;++i)
		{
			sum=sum+i;

		}
		System.out.println("Sum Of n Natural Numbers is: "+sum);

	}

}

package Loops;

public class ForLoop1 {

	public static void main(String[] args) {
		for(int i=1;i<11;i++) {
			System.out.println(i);
		}
		System.out.println("*************Reverse****************");
		for(int i=10;i>0;i--) {
			System.out.println(i);
		}
		int asciValue='a';
		System.out.println("*****************************");
		System.out.println("ASCII value is : "+asciValue);
	}
}
